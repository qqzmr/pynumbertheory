# -*-coding:utf-8-*-

import turtle

if __name__ == "__main__":
    turtle.showturtle()
    # (40，-69.28)、(-40，-69.28)、(-80，-9.8)、(-40，69)、(40，69)和(80，0)
    turtle.penup()
    turtle.goto(-200, 0)
    turtle.pendown()
    turtle.right(-60)
    turtle.forward(200)
    turtle.right(60)
    turtle.forward(200)
    turtle.right(60)
    turtle.forward(200)
    turtle.right(60)
    turtle.forward(200)
    turtle.right(60)
    turtle.forward(200)
    turtle.right(60)
    turtle.forward(200)

    turtle.done()
