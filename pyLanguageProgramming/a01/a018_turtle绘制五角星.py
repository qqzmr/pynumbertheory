# -*-coding:utf-8-*-

import turtle

if __name__ == "__main__":
    turtle.showturtle()
    turtle.penup()
    turtle.goto(-100, 0)
    turtle.pendown()
    a = 288 # 五边形
    a = 144 # 五角星
    turtle.forward(200)
    turtle.right(a)
    turtle.forward(200)
    turtle.right(a)
    turtle.forward(200)
    turtle.right(a)
    turtle.forward(200)
    turtle.right(a)
    turtle.forward(200)
    turtle.done()
